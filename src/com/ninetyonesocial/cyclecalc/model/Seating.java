package com.ninetyonesocial.cyclecalc.model;

public class Seating {

	private String saddleQuality; //Decent - Good - Excellent
	private String seatQuality; //Decent - Good - Excellent
	public String getSaddleQuality() {
		return saddleQuality;
	}
	public void setSaddleQuality(String saddleQuality) {
		this.saddleQuality = saddleQuality;
	}
	public String getSeatQuality() {
		return seatQuality;
	}
	public void setSeatQuality(String seatQuality) {
		this.seatQuality = seatQuality;
	}
	
}
